#Places in the world, list in og order
place = ["Seoul", "Kyoto", "Guangzhou", "Neuschwantein", "Athens"]
print (place)

#Sorting the list alphabetically
print (sorted(place))

#List in original order
print (place) 

#reverse list
print (sorted(place, reverse = True)) 

#List in og order
print (place)

#reverse order
place.reverse()
print (place)

#reverse order but back to og
place.reverse()
print (place)

#Sort alphabetically
place.sort()
print (place)

#Sort but in reverse
place.sort(reverse = True)
print (place)